package com.funding.dto;

public class Mem_state_codeVO {

	int code_state_num; //회원 상태 코드 번호
	String code_state; //회원 상태
	
	public Mem_state_codeVO() {}
	
	public Mem_state_codeVO(int code_state_num, String code_state) {
		super();
		this.code_state_num = code_state_num;
		this.code_state = code_state;
	}

	public int getCode_state_num() {
		return code_state_num;
	}
	public void setCode_state_num(int code_state_num) {
		this.code_state_num = code_state_num;
	}
	public String getCode_state() {
		return code_state;
	}
	public void setCode_state(String code_state) {
		this.code_state = code_state;
	}

	@Override
	public String toString() {
		return "Mem_state_codeVO [code_state_num=" + code_state_num + ", code_state=" + code_state + "]";
	}
	
}
