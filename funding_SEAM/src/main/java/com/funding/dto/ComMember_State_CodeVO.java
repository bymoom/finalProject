package com.funding.dto;

public class ComMember_State_CodeVO {

	private int com_state_code; //소상공인 가입 승인 여부 코드
	private String com_state_code_val; //소상공인 가입 승인 여부
	
	public ComMember_State_CodeVO() {}
	
	public ComMember_State_CodeVO(int com_state_code, String com_state_code_val) {
		super();
		this.com_state_code = com_state_code;
		this.com_state_code_val = com_state_code_val;
	}
	
	public int getCom_state_code() {
		return com_state_code;
	}

	public void setCom_state_code(int com_state_code) {
		this.com_state_code = com_state_code;
	}

	public String getCom_state_code_val() {
		return com_state_code_val;
	}

	public void setCom_state_code_val(String com_state_code_val) {
		this.com_state_code_val = com_state_code_val;
	}

	@Override
	public String toString() {
		return "ComMember_State_CodeVO [com_state_code=" + com_state_code + ", com_state_code_val=" + com_state_code_val
				+ "]";
	}
	
}
